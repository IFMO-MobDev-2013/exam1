package com.example.CarCleaning;

public class Car {
    String name, color, number, telephone;
    int time, box;

    public Car(String name, String color, String number, String telephone, int time, int box) {
        this.name = name;
        this.color = color;
        this.number = number;
        this.telephone = telephone;
        this.time = time;
        this.box = box;
    }
}
