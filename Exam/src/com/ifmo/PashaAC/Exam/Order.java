package com.ifmo.PashaAC.Exam;

public class Order {
    String mark;
    String colour;
    String auto_number;
    String time;
    String telephone;
    String box;

    public Order(String mark, String colour, String auto_number, String time, String telephone, String box) {
        this.mark = mark;
        this.colour = colour;
        this.auto_number = auto_number;
        this.time = time;
        this.telephone = telephone;
        this.box = box;
    }
}
